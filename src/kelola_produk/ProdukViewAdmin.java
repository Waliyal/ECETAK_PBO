    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
                                               
    if (idTerpilih == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih data produk di tabel terlebih dahulu!");
        return;
    }
    try {
        String nama = txtNama.getText();
        int stok = Integer.parseInt(txtStok.getText());
        int harga = Integer.parseInt(txtHarga.getText());

        // Memanggil fungsi update dari ProdukModel
        if (model.ubahProduk(idTerpilih, nama, stok, harga)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            txtNama.setText("");
            txtStok.setText("");
            txtHarga.setText("");
            idTerpilih = -1;
            isiTabelOtomatis();
        }
    } catch (NumberFormatException nfe) {
        javax.swing.JOptionPane.showMessageDialog(this, "Stok dan Harga harus berupa angka!");
    }

    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (idTerpilih == -1) { // Sesuaikan nama variabel idTerpilih Anda
        javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih data produk di tabel terlebih dahulu!");
        return;
    }
    int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus produk ini?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
    if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
        // Memanggil fungsi delete dari ProdukModel
        if (model.hapusProduk(idTerpilih)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            txtNama.setText("");
            txtStok.setText("");
            txtHarga.setText("");
            idTerpilih = -1;
            isiTabelOtomatis();
        }}
    }//GEN-LAST:event_btnHapusActionPerformed

