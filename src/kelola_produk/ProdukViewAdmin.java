    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
                                                  
    try {
        String nama = txtNama.getText();
        int stok = Integer.parseInt(txtStok.getText());
        int harga = Integer.parseInt(txtHarga.getText());

        if (nama.trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama barang tidak boleh kosong!");
            return;
        }

        // Memanggil fungsi insert dari ProdukModel
        if (model.tambahProduk(nama, stok, harga)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            // Reset form dan isi tabel kembali (seperti cbt.reset dan cbt.isitable)
            txtNama.setText("");
            txtStok.setText("");
            txtHarga.setText("");
            isiTabelOtomatis();
        }
    } catch (NumberFormatException nfe) {
        javax.swing.JOptionPane.showMessageDialog(this, "Stok dan Harga harus berupa angka murni!");
    }


    }//GEN-LAST:event_btnTambahActionPerformed

