    private void tblProdukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdukMouseClicked
        // TODO add your handling code here:                                     
    int baris = tblProduk.getSelectedRow();
    if (baris != -1) {
        // Ambil data baris yang diklik lalu naikkan ke teks form atas
        idTerpilih = Integer.parseInt(tblProduk.getValueAt(baris, 0).toString());
        txtNama.setText(tblProduk.getValueAt(baris, 1).toString());
        txtStok.setText(tblProduk.getValueAt(baris, 2).toString());
        txtHarga.setText(tblProduk.getValueAt(baris, 3).toString());
    }

    }//GEN-LAST:event_tblProdukMouseClicked

